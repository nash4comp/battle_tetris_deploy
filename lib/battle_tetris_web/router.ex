defmodule BattleTetrisWeb.Router do
  use BattleTetrisWeb, :router

  import BattleTetrisWeb.UserAuth

  pipeline :browser do
    plug :accepts, ["html"]
    plug :fetch_session
    plug :fetch_live_flash
    plug :put_root_layout, {BattleTetrisWeb.Layouts, :root}
    plug :protect_from_forgery
    plug :put_secure_browser_headers
    plug :fetch_current_user
  end

  pipeline :api do
    plug :accepts, ["json"]
  end

  scope "/", BattleTetrisWeb do
    pipe_through :browser

    get "/", PageController, :home
  end

  # Other scopes may use custom stacks.
  # scope "/api", BattleTetrisWeb do
  #   pipe_through :api
  # end

  # Enable LiveDashboard and Swoosh mailbox preview in development
  if Application.compile_env(:stream_chat, :dev_routes) do
    # If you want to use the LiveDashboard in production, you should put
    # it behind authentication and allow only admins to access it.
    # If your application does not have an admins-only section yet,
    # you can use Plug.BasicAuth to set up some basic authentication
    # as long as you are also using SSL (which you should anyway).
    import Phoenix.LiveDashboard.Router

    scope "/dev" do
      pipe_through :browser

      live_dashboard "/dashboard", metrics: BattleTetrisWeb.Telemetry
      forward "/mailbox", Plug.Swoosh.MailboxPreview
    end
  end

  ## Authentication routes

  scope "/", BattleTetrisWeb do
    pipe_through [:browser, :redirect_if_user_is_authenticated]

    live_session :redirect_if_user_is_authenticated,
      on_mount: [{BattleTetrisWeb.UserAuth, :redirect_if_user_is_authenticated}] do
      live "/users/register", UserRegistrationLive, :new
      live "/users/log_in", UserLoginLive, :new
    end

    post "/users/log_in", UserSessionController, :create
  end

  scope "/", BattleTetrisWeb do
    pipe_through [:browser, :require_authenticated_user]

    live_session :rooms,
      on_mount: [{BattleTetrisWeb.UserAuth, :ensure_authenticated}],
      layout: {BattleTetrisWeb.Layouts, :rooms} do
      live "/rooms", ChatLive.Root, :index
      live "/rooms/:id", ChatLive.Root, :show
      end
    end

  scope "/", BattleTetrisWeb do
    pipe_through [:browser]

    delete "/users/log_out", UserSessionController, :delete
  end
end
